#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const args = process.argv.slice(2);
const target = args[0];
const validBackends = new Set(['localstorage', 'indexeddb']);

if (!target || !validBackends.has(target)) {
  console.error('Usage: node scripts/set-storage-backend.js <localstorage|indexeddb>');
  process.exit(1);
}

const rootDir = __dirname;
const configPath = path.resolve(rootDir, '../storage-config.js');

const header = '// Auto-generated by scripts/set-storage-backend.js. Do not edit manually.\n';
const content = `${header}window.STORAGE_BACKEND_DEFAULT = '${target}';\n`;

try {
  fs.writeFileSync(configPath, content, 'utf8');
  console.log(`Default storage backend set to: ${target}`);
} catch (error) {
  console.error('Failed to update storage-config.js:', error.message);
  process.exit(1);
}
